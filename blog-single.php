                        À ce propos : qui peut l’utiliser ? Techniquement, tout le monde. Contrairement à la plupart des programmes de l’éditeur, Express est gratuit. Le programme peut être ouvert et utilisé avec un simple compte Adobe gratuit, mais pour avoir accès à ses modèles, polices et images de stock si pratiques, il faudra payer d’une manière ou d’une autre. La version complète de Creative Cloud Express est accessible aux utilisateurs qui bénéficient déjà d’un abonnement Creative Cloud. Dans le cas contraire, ils devront débourser 11,99 euros par mois ou 119,99 € par an pour obtenir la version complète. A noter qu’un essai gratuit de 3 mois est disponible. Cela reste une somme pour une série de modèles et d’actifs fantaisistes et une offre difficile à recommander au marché cible apparent des utilisateurs novices des médias sociaux. Mais il n’est pas rare qu’Adobe surévalue ses propres propriétés et services.
