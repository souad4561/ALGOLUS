                        <h2 class="title"><a href="#">Avec Create Cloud Express,Adobe veut séduire les jeunes créatifs</a></h2>
                        <p class="description">
                        Le dernier outil « gratuit » d’Adobe, Creative Cloud Express est destiné à des applications comme les stories Instagram, les cartes de visite, les affiches, les bannières, les newsletters ou encore les publications Facebook.                        </p>
                        <p class="description">
                        <strong> Les outils de création d’Adobe </strong>sont généralement les standards de leurs secteurs respectifs. Mais à moins de l’utiliser depuis des années ou d’investir dans une formation professionnelle, leur utilisation peut s’avérer frustrante. Le dernier-né de la longue parade d’applications de la société s’intitule « Creative Cloud Express ». Si le titre indéfini n’aide pas vraiment, il faut s’imaginer cela comme le premier outil de publication pour des enfants. Il est basé sur l’ancien programme Adobe Spark.

                         À la base, Express est un éditeur de mise en page auquel s’ajoutent quelques outils de retouche d’images pour faire bonne mesure. Il s’appuie fortement sur une collection de modèles et de ressources artistiques préétablis qui peuvent être mis en place, puis réorganisés et modifiés pour répondre aux besoins de ce qui intéresse l’utilisateur à ce moment-là. Il est principalement conçu pour les supports promotionnels en ligne : un bel en-tête ou une image d’accueil pour votre publication sur Facebook, par exemple. La version Web s’ouvre même sur une collection de formats et de modèles prédéfinis, au cas où vous craindriez de devoir partir de zéro. En outre, elle comprend « 20 000 polices Adobe de
                        qualité supérieure et 175 millions d’images Adobe Stock sous licence libre de droits ».
                        </p>
                        <p class="description">
                       <strong> Un programme multi-support</strong><br>
                      Express comprend également au moins quelques fonctionnalités de montage vidéo de base, ainsi que des services de partage entre les comptes Creative Suite (car Adobe souhaite vraiment que Creative Cloud soit avant tout une plateforme cloud). Les « actions rapides » tire parti de la puissance de traitement du cloud d’Adobe pour obtenir des versions simplifiées de solutions avancées de Photoshop et de Premiere, comme la suppression d’éléments en arrière-plan ou la fusion de vidéos.

                       Le plus intéressant, c’est qu’il s’agit d’un programme conçu dès le départ pour être multiplateforme. La version web fonctionne sur n’importe quel navigateur sous Windows, MacOS et Chrome OS, et naturellement Android et iOS ont leurs propres applications dédiées. Outre les adaptations évidentes à la forme et à la taille de l’écran sur mobile, toutes les versions d’Express ont accès aux mêmes outils, modèles et ressources. Adobe souhaite manifestement que les utilisateurs puissent se servir d’Express partout et en tout lieu.
                        </p>
