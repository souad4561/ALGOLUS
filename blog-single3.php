                        <h2 class="title"><a href="#"> Q'est-ce-qu'Adroid 12L qui se lance en beta cette  semail ?</a></h2>
                        <p class="description">
                        Android 12L débarque en bêta cette semaine. Cette version d’Android conçue pour les grands écrans, les tablettes et les appareils pliants se démarque légèrement d’Android 12 sous sa forme classique. On vous explique ce que cette version « L » a en plus.

                            Disponible à l’essai depuis hier au travers d’une première bêta en bonne et due forme, Android 12L ambitionne d’améliorer l’expérience d’utilisation et le multitâche sur tablettes, et plus globalement sur les appareils Android (pliants ou non) disposant d’écrans plus grands que la moyenne.

                                        Comme le précise The Verge, cette bêta reste encore assez tournée vers les développeurs, mais elle permet tout de même d’avoir une meilleure idée de ce que propose Android 12L… qui s’était jusque là surtout montré au travers d’une simple developer preview.

                    </p>
                        <h2 class="title"><a href="#"> Le multitâche simplifié</a></h2>

                        <p class="description">
                        Android 12L permet notamment une disposition en écran partagé permettant d’afficher deux applications côte à côte sur un grand écran, en format paysage. Cette répartition se fait facilement à l’aide d’un simple « glisser-déposer » depuis la barre des tâches. Notons d’ailleurs que la barre peut aussi être utilisée pour passer rapidement d’une application ouverte à l’autre.
                        Android 12L s’illustre aussi par l’ajout d’un affichage en deux colonnes permettant à certains éléments d’interface de laisser apparaître plus d’information en exploitant mieux l’espace disponible. Vos paramètres rapides et vos notifications peuvent ainsi apparaître côte à côte plutôt que l’un en dessous de l’autre

                        </p>
                        <h2 class="title"><a href="#"> Les grands écrans mieux exploités</a></h2>

                        <p class="description">
                        Pour l’instant, ces nouveautés dédiées aux grands écrans sont accessibles de manière parcellaire. Google explique que la bêta permet néanmoins d’en profiter sur les appareils à diagonales généreuses, compatibles avec Android 12, comme la nouvelle Lenovo Tab P12 Pro. Si vous souhaitez essayer ces nouveautés sans avoir d’appareils taillés pour en profiter, la piste de l’émulation reste toujours ouverte.
                        Google précise pour le reste qu’Android 12L peut être installé en bêta sur les smartphones Pixel allant du Pixel 3A jusqu’au Pixel 5A. Cela dit, les fonctionnalités pensées spécifiquement pour les grands écrans seront indisponibles… ce qui limite l’intérêt de l’installation.<br>
                        Source: https://www.clubic.com/  </p>
